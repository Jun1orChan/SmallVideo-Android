    /**
     * 取文件名或取文件扩展名
     *
     * @param path 文件路径
     * @param type 值:TYPE_FILENAME或TYPE_FILEEXT
     * @return 返回文件名或扩展名
     */
    private static String getFileNameOrExt(String path, String type) {
        File file = new File(path);
        String temp = file.getName();
        int ix = temp.lastIndexOf(".");
        if (ix < 0) {
            return temp;
        }
        if (type.equals(TYPE_FILENAME)) {
            return temp.substring(0, ix);
        } else if (type.equals(TYPE_FILEEXT)) {
            return temp.substring(ix, temp.length());
        }
        return temp;
    }

    /**
     * 写入文件
     *
     * @param in
     * @param file
     */
    public static void writeFile(InputStream in, File file) throws IOException {
        if (!file.getParentFile().exists()) {
            file.getParentFile().mkdirs();
        }
        if (file != null && file.exists()) {
            file.delete();
        }
        FileOutputStream out = new FileOutputStream(file);
        byte[] buffer = new byte[1024 * 128];
        int len = -1;
        while ((len = in.read(buffer)) != -1) {
            out.write(buffer, 0, len);
        }
        out.flush();
        out.close();
        in.close();
    }

    private static boolean isSpace(final String s) {
        if (s == null) {
            return true;
        }
        for (int i = 0, len = s.length(); i < len; ++i) {
            if (!Character.isWhitespace(s.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    private static String byte2FitMemorySize(final long byteNum) {
        if (byteNum < 0) {
            return "shouldn't be less than zero!";
        } else if (byteNum < 1024) {
            return String.format("%.3fB", (double) byteNum);
        } else if (byteNum < 1048576) {
            return String.format("%.3fKB", (double) byteNum / 1024);
        } else if (byteNum < 1073741824) {
            return String.format("%.3fMB", (double) byteNum / 1048576);
        } else {
            return String.format("%.3fGB", (double) byteNum / 1073741824);
        }
    }
