    /**
     * 根据文件后缀名获得对应的MIME类型。
     *
     * @param filepath
     * @return String
     */
    public static String getMIMEType(String filepath) {
        String type = "*/*";

        /* 获取文件的后缀名 */
        String end = "." + FileUtil.getFileExt(filepath).toLowerCase();
        for (int i = 0; i < MIME_TYPE.length; i++) {
            if (end.equals(MIME_TYPE[i][0])) {
                type = MIME_TYPE[i][1];
            }
        }
        return type;
    }

