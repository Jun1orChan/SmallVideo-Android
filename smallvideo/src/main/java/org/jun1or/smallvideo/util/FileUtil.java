    /**
     * 获取目录长度
     *
     * @param dir 目录
     * @return 目录长度
     */
    public static long getDirLength(final File dir) {
        if (!isDir(dir)) {
            return -1;
        }
        long len = 0;
        File[] files = dir.listFiles();
        if (files != null && files.length != 0) {
            for (File file : files) {
                if (file.isDirectory()) {
                    len += getDirLength(file);
                } else {
                    len += file.length();
                }
            }
        }
        return len;
    }


    /**
     * 读取Assert文件内容
     *
     * @param c
     * @param fileName 文件名
     * @return 文件内容String
     */
    public static String readAssertFile(Context c, String fileName) {
        String result = "";
        InputStream in = null;
        InputStreamReader isr = null;
        BufferedReader reader = null;
        try {
            in = c.getAssets().open(fileName);
            isr = new InputStreamReader(in, Charset.forName("UTF-8"));
            reader = new BufferedReader(isr);
            String temp = "";
            while ((temp = reader.readLine()) != null) {
                result += temp;
            }
        } catch (IOException e) {
            e.printStackTrace();
            result = "";
        } finally {
            try {
                if (reader != null) {
                    reader.close();
                    reader = null;
                }
                if (isr != null) {
                    isr.close();
                    isr = null;
                }
                if (in != null) {
                    in.close();
                    in = null;
                }
            } catch (IOException e) {
                // TODO Auto-generated catch block
                e.printStackTrace();
            }
        }
        return result;
    }


